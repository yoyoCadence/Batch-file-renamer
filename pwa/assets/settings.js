export function t(language, key, params = {}) {
  const dictionary = TRANSLATIONS[language] || TRANSLATIONS[DEFAULT_SETTINGS.language];
  const fallback = TRANSLATIONS.en || {};
  const template = dictionary[key] ?? fallback[key] ?? key;
  return String(template).replace(/\{(\w+)\}/g, (_, name) => params[name] ?? `{${name}}`);
}

export function normalizeSettings(input = {}) {
  return {
    language: hasId(LANGUAGES, input.language) ? input.language : DEFAULT_SETTINGS.language,
    template: hasId(TEMPLATES, input.template) ? input.template : DEFAULT_SETTINGS.template,
    theme: hasId(THEMES, input.theme) ? input.theme : DEFAULT_SETTINGS.theme,
    petEnabled: typeof input.petEnabled === "boolean" ? input.petEnabled : DEFAULT_SETTINGS.petEnabled,
    petType: hasId(PETS, input.petType) ? input.petType : DEFAULT_SETTINGS.petType
  };
}

export function loadSettings(storage = globalThis.localStorage) {
  if (!storage) {
    return { ...DEFAULT_SETTINGS };
  }
  try {
    const raw = storage.getItem("batch-file-renamer.settings");
    return normalizeSettings(raw ? JSON.parse(raw) : DEFAULT_SETTINGS);
  } catch {
    return { ...DEFAULT_SETTINGS };
  }
}

export function saveSettings(settings, storage = globalThis.localStorage) {
  const normalized = normalizeSettings(settings);
  if (storage) {
    storage.setItem("batch-file-renamer.settings", JSON.stringify(normalized));
  }
  return normalized;
}

export function applyTranslations(root, language) {
  const scope = root?.querySelectorAll ? root : document;
  const lang = normalizeSettings({ language }).language;
  if (scope.documentElement) {
    scope.documentElement.lang = lang;
  }
  scope.querySelectorAll("[data-i18n]").forEach((element) => {
    element.textContent = t(lang, element.dataset.i18n);
  });
  scope.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
    element.setAttribute("placeholder", t(lang, element.dataset.i18nPlaceholder));
  });
  scope.querySelectorAll("[data-i18n-aria-label]").forEach((element) => {
    element.setAttribute("aria-label", t(lang, element.dataset.i18nAriaLabel));
  });
  scope.querySelectorAll("[data-i18n-title]").forEach((element) => {
    element.setAttribute("title", t(lang, element.dataset.i18nTitle));
  });
}

export function applyAppearance(settings, body = document.body) {
  const normalized = normalizeSettings(settings);
  body.dataset.template = normalized.template;
  body.dataset.theme = normalized.theme;
}

function hasId(collection, id) {
  return collection.some((item) => item.id === id);
}
